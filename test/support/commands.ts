/** a standard foobar function */
export function foobar(foo: string) {
  return `foo may be ${foo}, but bar is better!`;
}

/**
 * Anonymous functions work too
 * 
 * @param a an important number
 * @param b a secret number
 * @returns a secret and import number
 */
export const anonMultiplication = (a: number, b: number) => a*b

